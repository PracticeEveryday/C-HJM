#include <stdio.h>

int main()
{
  int a, b;

  // c만 포인터고 d는 인트형이다.
  int *c, d;

  // 포인터 자료형을 표현하는 것처럼 보일 수 있따.
  // 하지만 f만 인트형 포인터이고 g는 int형이다.
  int *f, g;

  // 모두 포인터로 지정한다.
  int *h, *i;

  // C++는 무조건 두개로 쪼개라라고함.
  int *a;
  int *b;

  // 같이 프로젝트를 하시는 분들이랑 코딩 스타일을 맞추면 된다!!
  // 뭐가 올바른 것이 아니다!
}